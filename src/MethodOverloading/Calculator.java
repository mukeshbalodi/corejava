package MethodOverloading;

public class Calculator {
	public static int add(int a , int b ) {
		return (a+b);
	}
	public static int sub(int a, int b) {
		return (a-b);
		
	}
	public static float div (float a,float b) {
		return (a/b);
	}
	public static int multiply (int a, int b) {
		return (a*b);
	}
	
		
	}

	
	


